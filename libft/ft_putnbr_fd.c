#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	min;

	min = '-';
	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		n = 147483648;
	}
	else if (n < 0)
	{
		write(fd, &min, 1);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	n = n % 10 + 48;
	write(fd, &n, 1);
}
